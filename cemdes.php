            <h3 class="subtitulos" >Consejo Regional Ecuatoriano del World Business Council for Sustainable Development</h3>
            <p>Desde 2001 Cemdes es el socio ecuatoriano del Consejo Empresarial Mundial para el Desarrollo Sostenible (WBCSD), organización líder a nivel mundial en desarrollo sostenible empresarial.</p>
            <p>El WBCSD es una coalición de 200 empresas internacionales unidas por una visión común: el desarrollo sostenible ha de alcanzarse a través del crecimiento económico, el equilibrio ambiental y el progreso social.</p>
            <p>La Red Regional del WBCSD aporta valor a sus Empresas Miembro al:</p>
