                    <li>Permitir el acceso a los programas del WBCSD: intercambio de experiencias, herramientas y material de construcción de capacidad.</li>
                    <li>Facilitar la comunicación con los principales actores internacionales y plataformas que trabajan por los negocios y el desarrollo sostenible.</li>
                    <li>Activar contactos y vínculos con las corporaciones globales, fomentando al aprendizaje entre países y sectores.</li>
