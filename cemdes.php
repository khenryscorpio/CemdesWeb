                        <h3 class="subtitulos">CONSULTORES Y/O ESPECIALISTAS EN GESTI&#211;N LEGAL AMBIENTAL Y SOCIO AMBIENTAL.</h3>
                        <p>Cemdes, con la finalidad de atender los requerimientos de sus usuarios, respecto a empresas que están en capacidad de efectuar auditorias ambientales y de ofrecer servicios de asesoría en el desarrollo e implantación de programas de gestión socio ambiental, estableció el Proceso de Alineación a los Principios de Cemdes.

                            Este proceso de selección permite a Cemdes asegurar, confidencialidad, profesionalismo, eficiencia y eficacia por parte de los prestadores de estos servicios.

                            Esta alineación no constituye un procedimiento legal sino un compromiso institucional entre el ofertante y el receptor del servicio. La base de este proceso son los Principios de Cemdes.

                            Todos los consultores (as) y/o especialistas están en capacidad de efectuar estudios Sociales (IRSE, MRSA, MC) y Ambientales (EIA, AAi, AAc, DA, IA, ARO) y de ofrecer servicios de asesoría en el desarrollo e implantación de programas de gestión socio ambiental y análisis de riesgo.                    </p>

